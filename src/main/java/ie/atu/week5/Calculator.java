package ie.atu.week5;
import java.util.Scanner;

public class Calculator {
    public int add(int x, int y) {
        int total = x + y;
        return(total);
    }

    public int subtract(int x, int y) {
        int total = x - y;
        return(total);
    }

    public float divide(float x, float y) {
        float total = x / y;
        return(total);
    }
}
